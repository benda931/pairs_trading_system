@dataclass
class PortfolioState:
    """Complete portfolio state snapshot."""
    timestamp: datetime
    total_capital: float
    gross_exposure: float
    net_exposure: float
    n_active_pairs: int
    allocations: list[SpreadAllocation] = field(default_factory=list)
