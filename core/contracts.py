"""
core/contracts.py — Platform-Wide Domain Contracts, Types, and Protocols
=========================================================================

THIS IS THE SINGLE SOURCE OF TRUTH for all domain types used across the
pairs trading platform. All other modules should import types from here.

Design principles:
- All public types are dataclasses or enums (never bare dicts or tuples)
- All validation pipelines produce typed results with explicit rejection reasons
- All pair/spread states are tracked via a formal state machine (PairLifecycleState)
- All protocols define the minimal interface a component must satisfy
- No implicit global state; everything flows through typed contracts

Pair Lifecycle:
  CANDIDATE → VALIDATED → ACTIVE → SUSPENDED → RETIRED
      ↕            ↕           ↕
   REJECTED    PAUSED      BROKEN

"""

