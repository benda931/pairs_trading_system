@dataclass
class DatasetSpec:
    """Specification for building a ML training dataset."""
    dataset_id: str
    task: str
    feature_names: list[str]
    target_name: str
    train_start: datetime
    train_end: datetime
    embargo_days: int = 5       # Gap between train and test to prevent leakage
    purge_days: int = 10        # Purge overlapping train samples near test boundary
    min_samples: int = 100
    max_lookahead_days: int = 0  # Must be 0 for production; > 0 only for research flags
    created_at: datetime = field(default_factory=datetime.utcnow)
    notes: str = ""

    def validate(self) -> list[str]:
        """Return list of validation errors (empty = valid)."""
        errors = []
        if self.max_lookahead_days > 0:
            errors.append(
                f"LEAKAGE WARNING: max_lookahead_days={self.max_lookahead_days}. "
                "Set to 0 for production-grade research."
            )
        if self.embargo_days < 1:
            errors.append("embargo_days should be >= 1 for time-series data.")
        if self.train_end <= self.train_start:
            errors.append("train_end must be after train_start.")
        return errors


