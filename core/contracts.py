@dataclass
class RiskBreach:
    """A single risk limit breach event."""
    breach_type: RiskBreachType
    current_value: float
    limit_value: float
    severity: float             # 0-1 (1 = kill switch level)
    pair_id: Optional[PairId] = None  # None = portfolio-level breach
    description: str = ""
    detected_at: datetime = field(default_factory=datetime.utcnow)
    requires_action: bool = True

    @property
    def excess_pct(self) -> float:
        """How far over the limit, as a percentage."""
        if self.limit_value == 0:
            return float("inf")
        return (self.current_value - self.limit_value) / abs(self.limit_value) * 100


@dataclass
class ExperimentConfig:
    """Configuration for a research experiment (walk-forward, backtests, etc.)."""
    experiment_id: str
    description: str = ""
    pair_id: Optional[PairId] = None
    universe_name: str = ""
    train_start: Optional[datetime] = None
    train_end: Optional[datetime] = None
    test_start: Optional[datetime] = None
    test_end: Optional[datetime] = None
    params: dict = field(default_factory=dict)
    tags: list[str] = field(default_factory=list)
    created_at: datetime = field(default_factory=datetime.utcnow)
    random_seed: int = 42

    def to_dict(self) -> dict:
        d = {k: v for k, v in self.__dict__.items()}
        if self.pair_id:
            d["pair_id"] = self.pair_id.label
        for dt_key in ("train_start", "train_end", "test_start", "test_end", "created_at"):
            if d.get(dt_key):
                d[dt_key] = d[dt_key].isoformat()
        return d


