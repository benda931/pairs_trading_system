@dataclass
class PairValidationReport:
    """
    Full validation report for a candidate pair.

    This is the authoritative record of WHY a pair was accepted, warned,
    or rejected. Every rejection reason is explicitly captured here.
    """
    pair_id: PairId
    result: ValidationResult
    timestamp: datetime = field(default_factory=datetime.utcnow)

    # Individual test results
    tests: list[ValidationTest] = field(default_factory=list)

    # Summary metrics (populated even on rejection for diagnostics)
    correlation: float = np.nan
    adf_pvalue: float = np.nan
    half_life_days: float = np.nan
    hurst_exponent: float = np.nan
    spread_vol: float = np.nan
    residual_vol: float = np.nan
    hedge_ratio: float = np.nan
    hedge_ratio_method: HedgeRatioMethod = HedgeRatioMethod.OLS
    cointegration_pvalue: float = np.nan
    rolling_stability: float = np.nan   # 0-1 score of hedge ratio stability

    # Rejection reasons (empty if PASS)
    rejection_reasons: list[str] = field(default_factory=list)
    # Warning reasons (advisory; trade with reduced size)
    warning_reasons: list[str] = field(default_factory=list)

    # Metadata
    train_start: Optional[datetime] = None
    train_end: Optional[datetime] = None
    n_obs: int = 0
    spread_model: SpreadModel = SpreadModel.STATIC_OLS

    @property
    def is_tradable(self) -> bool:
        return self.result in (ValidationResult.PASS, ValidationResult.WARN)

    @property
    def failed_hard_tests(self) -> list[ValidationTest]:
        return [t for t in self.tests if not t.passed and t.is_hard]

    @property
    def failed_soft_tests(self) -> list[ValidationTest]:
        return [t for t in self.tests if not t.passed and not t.is_hard]

    def to_dict(self) -> dict:
        return {
            "pair": self.pair_id.label,
            "result": self.result.value,
            "correlation": self.correlation,
            "adf_pvalue": self.adf_pvalue,
            "half_life_days": self.half_life_days,
            "hurst_exponent": self.hurst_exponent,
            "hedge_ratio": self.hedge_ratio,
            "cointegration_pvalue": self.cointegration_pvalue,
            "rolling_stability": self.rolling_stability,
            "n_obs": self.n_obs,
            "rejection_reasons": self.rejection_reasons,
            "warning_reasons": self.warning_reasons,
            "timestamp": self.timestamp.isoformat(),
        }


@dataclass
class SpreadDefinition:
    """
    Complete definition of a spread/residual series.

    Encapsulates how the spread is constructed, normalized, and interpreted.
    """
    pair_id: PairId
    model: SpreadModel
    hedge_ratio: float
    hedge_ratio_method: HedgeRatioMethod
    intercept: float = 0.0
    half_life_days: float = np.nan
    mean: float = 0.0
    std: float = 1.0

    # Rolling parameters (if model is ROLLING or KALMAN)
    window: int = 60
    kalman_observation_cov: float = 0.001
    kalman_transition_cov: float = 0.0001

    # Factor neutralization
    factor_names: list[str] = field(default_factory=list)
    factor_loadings: list[float] = field(default_factory=list)

    # Estimation metadata
    estimated_at: Optional[datetime] = None
    train_start: Optional[datetime] = None
    train_end: Optional[datetime] = None

    def compute_spread(
        self,
        price_x: pd.Series,
        price_y: pd.Series,
    ) -> pd.Series:
        """Compute the raw (un-normalized) spread series."""
        raw = price_x - self.hedge_ratio * price_y - self.intercept
        raw.name = f"spread_{self.pair_id.label}"
        return raw

    def compute_zscore(
        self,
        price_x: pd.Series,
        price_y: pd.Series,
        *,
        rolling_window: Optional[int] = None,
    ) -> pd.Series:
        """Compute the z-scored spread."""
        raw = self.compute_spread(price_x, price_y)
        if rolling_window and rolling_window > 0:
            mu = raw.rolling(rolling_window).mean()
            sigma = raw.rolling(rolling_window).std()
        else:
            mu = self.mean
            sigma = self.std
        z = (raw - mu) / sigma.clip(lower=1e-8) if isinstance(sigma, pd.Series) else (raw - mu) / max(sigma, 1e-8)
        z.name = f"z_{self.pair_id.label}"
        return z


