@dataclass
class AgentTask:
    """
    Typed task contract for any agent.

    Agents receive AgentTask objects and produce AgentResult objects.
    No agent should accept or produce bare dicts.
    """
    task_id: str
    agent_name: str
    task_type: str              # e.g. "validate_pair", "classify_regime"
    payload: dict               # Task-specific parameters (agent-defined schema)
    priority: int = 5           # 1=highest, 10=lowest
    requires_approval: bool = False
    correlation_id: str = ""    # For tracing chains of tasks
    created_at: datetime = field(default_factory=datetime.utcnow)
    deadline: Optional[datetime] = None
    metadata: dict = field(default_factory=dict)


@dataclass
class AgentResult:
    """Typed result from an agent task execution."""
    task_id: str
    agent_name: str
    status: AgentStatus
    output: dict = field(default_factory=dict)  # Agent-defined structured output
    error: str = ""
    warnings: list[str] = field(default_factory=list)
    audit_trail: list[str] = field(default_factory=list)  # Key decision steps
    completed_at: datetime = field(default_factory=datetime.utcnow)
    duration_ms: float = 0.0
    requires_human_review: bool = False

    @property
    def succeeded(self) -> bool:
        return self.status == AgentStatus.COMPLETED

    def log(self, step: str) -> None:
        """Add an auditable step to the trail."""
        self.audit_trail.append(f"{datetime.utcnow().isoformat()} | {step}")


