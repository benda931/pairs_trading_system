    drawdown_from_peak: float = 0.0
    daily_pnl: float = 0.0
    realized_vol: float = np.nan

    @property
    def gross_leverage(self) -> float:
        if self.total_capital <= 0:
            return 0.0
        return self.gross_exposure / self.total_capital

    @property
    def net_leverage(self) -> float:
        if self.total_capital <= 0:
            return 0.0
        return abs(self.net_exposure) / self.total_capital


