class PairLifecycleState(str, enum.Enum):
    """Canonical states for a pair/spread relationship.

    State machine transitions:
        CANDIDATE  → VALIDATED (passed all statistical + economic tests)
        CANDIDATE  → REJECTED  (failed validation; rejection_reasons populated)
        VALIDATED  → ACTIVE    (allocated capital, signals live)
        VALIDATED  → PAUSED    (monitoring but not trading; e.g. low liquidity)
        ACTIVE     → SUSPENDED (temporarily halted; e.g. corporate action, regime)
        ACTIVE     → BROKEN    (relationship broken; structural break detected)
        ACTIVE     → RETIRED   (gracefully exited; relationship still valid but deprioritized)
        SUSPENDED  → ACTIVE    (re-enabled after resolution)
        SUSPENDED  → BROKEN    (break confirmed during suspension)
        BROKEN     → RETIRED   (permanent retirement after confirmed break)
        BROKEN     → CANDIDATE (re-evaluation after major reset; rare)
    """
    CANDIDATE  = "CANDIDATE"   # Generated by universe screen, not yet validated
    VALIDATED  = "VALIDATED"   # Passed all statistical + economic tests
    ACTIVE     = "ACTIVE"      # Live: capital allocated, signals generated
    PAUSED     = "PAUSED"      # Monitored but not traded (e.g., low liquidity window)
    SUSPENDED  = "SUSPENDED"   # Temporarily halted (regime, event, circuit-breaker)
    BROKEN     = "BROKEN"      # Structural break detected; relationship invalidated
    RETIRED    = "RETIRED"     # Gracefully exited; no longer in active universe
    REJECTED   = "REJECTED"    # Failed validation; logged with rejection_reasons


