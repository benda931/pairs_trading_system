@dataclass(frozen=True)
class PairId:
    """Canonical, hashable pair identifier. sym_x < sym_y lexicographically."""
    sym_x: str
    sym_y: str

    def __post_init__(self):
        # Enforce canonical ordering to prevent (A,B) and (B,A) duplicates
        if self.sym_x > self.sym_y:
            orig_x = self.sym_x
            object.__setattr__(self, "sym_x", self.sym_y)
            object.__setattr__(self, "sym_y", orig_x)

    @property
    def label(self) -> str:
        return f"{self.sym_x}/{self.sym_y}"

    def __str__(self) -> str:
        return self.label

    def __contains__(self, symbol: str) -> bool:
        return symbol in (self.sym_x, self.sym_y)

    def as_tuple(self) -> tuple[str, str]:
        return (self.sym_x, self.sym_y)

    @classmethod
    def from_label(cls, label: str) -> "PairId":
        """Parse a 'SYM_X/SYM_Y' label. Auto-sorts for canonical order."""
        parts = label.split("/")
        if len(parts) != 2 or not all(parts):
            raise ValueError(f"Invalid PairId label: {label!r}. Expected 'SYM_X/SYM_Y'.")
        return cls(parts[0], parts[1])


